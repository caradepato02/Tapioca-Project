    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed

        int iDialogResult = JOptionPane.showConfirmDialog(this, "Remove course?", "Confirm", JOptionPane.OK_CANCEL_OPTION);
        if (iDialogResult == 0) {
            Login.CoursesList.removeCourse();
            JOptionPane.showMessageDialog(this, "Course removed", "Done!", JOptionPane.INFORMATION_MESSAGE);
            Login.UsersList.nActiveUser.setiIdexCourse(0);
            Login.hHome.repintar();
            Login.saveUser();
            Login.saveCourser();
        }
        dispose();
    }//GEN-LAST:event_btnRemoveActionPerformed

