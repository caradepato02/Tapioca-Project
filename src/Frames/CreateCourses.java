    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        sCourseName = txtName.getText();
        sNumber = txtPhone.getText();
        sDescription = txaDescrip.getText();

        //Verifica si el usuario tiene un curso
        if (Login.UsersList.nActiveUser.getiIdexCourse() != 0) {//Si tiene curso, edita el curso
            Login.CoursesList.nUserCourse.setsDescription(sDescription);
            Login.CoursesList.nUserCourse.setsNumber(sNumber);
            Login.CoursesList.nUserCourse.setsCourseName(sCourseName);
            JOptionPane.showMessageDialog(this, "course Saved", "Done!", JOptionPane.INFORMATION_MESSAGE);
        } else {//Si no tiene curso, crea uuno nuevo
            Login.UsersList.nActiveUser.setiIdexCourse((Login.iCountIndex) + 1);
            Nodes nNode = new Nodes(Login.sName, Login.sLastname, (Login.iCountIndex) + 1, sCourseCategory.toLowerCase(), sCourseName, sNumber, sDescription);
            Login.CoursesList.add(nNode);
            Login.CoursesList.nUserCourse = nNode;
            JOptionPane.showMessageDialog(this, "course created", "Done!", JOptionPane.INFORMATION_MESSAGE);
            Login.saveUser();
            Login.saveCourser();
        }
        dispose();
    }//GEN-LAST:event_btnCreateActionPerformed

